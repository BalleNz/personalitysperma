def get_relationships_description(
    mbti_type_1: str,
    mbti_type_2: str
) -> str:
    """Возвращает описание взаимоотношений MBTI между двумя людьми"""

    short_description, long_description, short_term_score, long_term_score, human_values, human_business, how_to_love = SOCIONICS_RELATIONSHIPS.get(
        mbti_type_1
    ).get(
        mbti_type_2
    )

    verdict = ""

    match (short_term_score, long_term_score):
        # Высокая химия + высокая долгосрочная совместимость
        case (x, y) if x > 75 and y > 80:
            verdict = "<u>Прогноз</u>:\nочень сильная искра + отличные шансы на долгую и счастливую пару. Один из лучших сценариев: быстрое влечение и хорошая основа для будущего."

        # Высокая химия + хорошая долгосрочная
        case (x, y) if x > 70 and y > 65:
            verdict = "<u>Прогноз</u>:\nсильное взаимное притяжение с самого начала + хорошие перспективы на длительные отношения. Есть над чем поработать, но потенциал очень высокий."

        # Средняя/высокая химия + средняя долгосрочная
        case (x, y) if x > 60 and y > 50:
            verdict = "<u>Прогноз</u>:\nхорошая химия и комфорт на старте, но для долгосрочных отношений потребуется осознанная работа над различиями. Может получиться крепкая пара при взаимном желании."

        # Высокая химия + низкая долгосрочная (самый частый «горячий, но недолгий» сценарий)
        case (x, y) if x > 70 and y <= 40:
            verdict = "<u>Прогноз</u>:\nочень сильное влечение и яркая химия, но крайне низкая вероятность долгосрочного счастья. Классический сценарий «бурный роман, который быстро гаснет»."

        # Низкая химия + высокая долгосрочная (медленное развитие, но крепкий союз)
        case (x, y) if x <= 40 and y > 75:
            verdict = "<u>Прогноз</u>:\nсразу искры почти нет, влечение развивается медленно. Однако фундамент очень хороший — при терпении и постепенном сближении может вырасти в очень стабильную и счастливую пару."

        # Всё среднее
        case (x, y) if 45 <= x <= 65 and 45 <= y <= 65:
            verdict = "<u>Прогноз</u>:\nсредняя химия и средняя долгосрочная совместимость. Отношения скорее всего будут «нормальными», без сильных взлётов и падений. Всё зависит от того, насколько оба готовы вкладываться."

        # Низкая химия + низкая долгосрочная
        case (x, y) if x <= 35 and y <= 35:
            verdict = "<u>Прогноз</u>:\nочень слабое влечение и крайне низкая совместимость в долгосрочной перспективе. Скорее всего будет скучно, напряжённо или быстро надоест."

        # Остальные комбинации (переходные / не самые яркие)
        case _:
            if short_term_score > long_term_score + 25:
                verdict = "<u>Прогноз</u>:\nсильная начальная химия, но долгосрочная перспектива существенно хуже. Скорее всего яркое начало и довольно быстрое угасание / разочарование."
            elif long_term_score > short_term_score + 25:
                verdict = "<u>Прогноз</u>:\nсразу сильной искры нет, но фундамент для длительных отношений хороший. Может вырасти в тёплую и стабильную пару, если дать время."
            else:
                verdict = "<u>Прогноз</u>:\nсредний баланс между химией и долгосрочной совместимостью. Без ярких эмоций, но и без сильных конфликтов. Стабильность будет зависеть от усилий обоих."

    text: str = (
        f"<b>⏳ В краткосрочных отношениях:</b> {short_term_score} / 100\n"
        f"<blockquote>— {short_description}</blockquote>\n\n"
        
        
        f"<b>⌛ В долгосрочных отношениях:</b> {long_term_score} / 100\n"
        f"<blockquote>— {long_description}</blockquote>\n\n"
        
        "<b>👀 Жизненные ценности:</b>\n"
        f"<blockquote>— {human_values}</blockquote>\n\n"
        
        "<b>🧠 Тип мышления:</b>\n"
        f"<blockquote>— {human_business}.</blockquote>\n\n"
        
        f"<b>Как вызвать доверие у {mbti_type_2}?</b>\n"
        f"— {how_to_love}\n\n"
        
        f"{verdict}"
    )

    return text


def get_relationships_briefly(
        mbti: str
) -> str:
    """Взаимоотношения с другими типами со схемы"""

    best_relationships: list[tuple[str, str]] = mbti_relationships_briefly.get(mbti).get('best_relationships')
    conflicts_types: list[tuple[str, str]] = mbti_relationships_briefly.get(mbti).get('conflict')

    briefly_description: str = mbti_relationships_briefly.get(mbti).get('about_relationships')
    enjoy_things: str = mbti_relationships_briefly.get(mbti).get('enjoy_things')
    terrible_things: str = mbti_relationships_briefly.get(mbti).get('terrible_thing')

    best_pairs_text: str = ""
    for i, data in enumerate(
            sorted(best_relationships, key=lambda x: -x[2]),
            start=1
    ):
        verdict, mbti_type, score, description = data
        best_pairs_text += f"<b>{i}. {mbti_type}:</b> {verdict}; {description}"
        if i != len(best_relationships):
            best_pairs_text += "\n\n"

    worst_pairs_text: str = ""
    for i, data in enumerate(
            sorted(conflicts_types, key=lambda x: x[2]),
            start=1
    ):
        mbti_type, verdict, score, description = data
        worst_pairs_text += f"<b>{i}. {mbti_type}:</b> {verdict}; {description}"
        if i != len(conflicts_types):
            worst_pairs_text += "\n\n"

    text: str = (
        f"<b>❤️‍🔥 Лучшие пары <i>(в порядке убывания)</i>:</b>\n"
        f"<blockquote>"
        f"{best_pairs_text}"
        f"</blockquote>\n\n"
        
        f"{enjoy_things}\n\n"
        
        f"<b>💩️ Конфликторы:</b>\n"
        f"<blockquote>"
        f"{worst_pairs_text}"
        f"</blockquote>\n\n"
        
        f"{terrible_things}\n\n"
        
        f"{briefly_description}"
    )
    return text
